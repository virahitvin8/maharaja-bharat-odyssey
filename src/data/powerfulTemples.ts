export function getTempleStyle(style: string): string {
  const styles: Record<string, string> = {
    gupta: 'Simple square sanctum with flat roof, emerging shikhara',
    chalukya: 'Fusion of North and South Indian styles, pillared halls',
    pallava: 'Rock-cut rathas and monolithic vimanas',
    rashtrakuta: 'Monolithic rock-cut, Mount Kailash representation',
    chola: 'Massive vimanas, towering gopurams, bronze sculptures',
    somavamsi: 'Kalinga style — curvilinear deul tower',
    chandela: 'Multiple shikharas, erotic sculptures, sandstone',
    solanki: 'Stepwells, solar alignments, intricate pillared halls',
    ganga: 'Massive chariot designs, Kalinga tradition',
    hoysala: 'Star-shaped platforms, soapstone filigree, thousands of deities',
    kakatiya: 'Floating bricks, Nandi mandapas, porous lightweight stone',
    shaka: 'Ancient rock-cut, earliest functional shrines',
    vijayanagara: 'Towering gopurams, thousand-pillared halls',
  }
  return styles[style] || 'Ancient Indian temple architecture'
}
